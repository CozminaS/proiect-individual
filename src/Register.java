    private void registerUser(String utilizator, String parola, String prenume, String nume, String adresa, String nrTelefon, String email, String lvlpermi) {
        Connection dbconn= ConectareDB.connectDB();
        if(dbconn != null){
        try {
            PreparedStatement st=(PreparedStatement)
                    dbconn.prepareStatement("INSERT INTO users(utilizator,parola,nume,prenume,adresa,telefon,email,lvlpermi) VALUES(?,?,?,?,?,?,?,?)");   
            st.setString(1, utilizator);
            st.setString(2, parola);
            st.setString(3, nume);
            st.setString(4, prenume);
            st.setString(5, adresa);
            st.setString(6, nrTelefon);
            st.setString(7, email);
            st.setString(8,lvlpermi);
            int res= st.executeUpdate();
          
                JOptionPane.showMessageDialog(this, "Datele despre utilizator au fost salvate cu succes","Suces",JOptionPane.INFORMATION_MESSAGE);
            
        } catch (SQLException ex) {
            Logger.getLogger(Conectare.class.getName()).log(Level.SEVERE, null, ex);
        }
    }else
        {
            System.out.println("Conexiunea nu este disponibila.");
        }
    }
