    private void actiuneRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actiuneRegisterActionPerformed
        // TODO add your handling code here:
        Utilizator utilizator=new Utilizator();

        utilizator.setUtilizator(registerUtilizator.getText());
        utilizator.setNume(registerNume.getText());
        utilizator.setPrenume(registerPrenume.getText());
        utilizator.setNrTelefon(registerTelefon.getText());
        utilizator.setEmail(registerEmail.getText());
        utilizator.setAdresa(registerAdresa.getText());
        utilizator.setParola(registerParola.getText());
        utilizator.setNivelPermisiune("0");
        if(utilizator.getUtilizator().isEmpty() || utilizator.getParola().isEmpty() || utilizator.getNume().isEmpty() || utilizator.getPrenume().isEmpty() || utilizator.getAdresa().isEmpty() || utilizator.getNrTelefon().isEmpty() || utilizator.getEmail().isEmpty() )
        {
            JOptionPane.showMessageDialog(this, "Toate campurile trebuie copletate","Eroare",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            //start the logIn process.
            registerUser(utilizator.getUtilizator(),utilizator.getParola(),utilizator.getPrenume(), utilizator.getNume(),utilizator.getAdresa(),utilizator.getNrTelefon(),utilizator.getEmail(),  utilizator.getNivelPermisiune());
        }
    }//GEN-LAST:event_actiuneRegisterActionPerformed

